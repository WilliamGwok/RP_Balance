function [xa, ya, xb, yb, xc, yc, xd, yd, xe, ye] = Func_Cal_Coordinate(phi1, phi4)

global l1 l2 l3 l4 l5;%单位为m

xa = 0;
ya = 0;
xb = l1*cos(phi1);
yb = l1*sin(phi1);
xc = l1*cos(phi1) + l2*cos(2*atan((((l1^2 - 2*cos(phi1 - phi4)*l1*l4 - 2*cos(phi1)*l1*l5 ...
       - l2^2 + 2*l2*l3 - l3^2 + l4^2 + 2*cos(phi4)*l4*l5 + l5^2)*(- l1^2 + 2*cos(phi1 - phi4)*l1*l4 ...
       + 2*cos(phi1)*l1*l5 + l2^2 + 2*l2*l3 + l3^2 - l4^2 - 2*cos(phi4)*l4*l5 - l5^2))^(1/2) - 2*l1*l2*sin(phi1) ...
       + 2*l2*l4*sin(phi4))/(l1^2 - 2*cos(phi1)*l1*l2 - 2*cos(phi1 - phi4)*l1*l4 - 2*cos(phi1)*l1*l5 + l2^2 ...
       + 2*cos(phi4)*l2*l4 + 2*l2*l5 - l3^2 + l4^2 + 2*cos(phi4)*l4*l5 + l5^2)));
yc = l2*sin(2*atan((((l1^2 - 2*cos(phi1 - phi4)*l1*l4 - 2*cos(phi1)*l1*l5 - l2^2 + 2*l2*l3 - l3^2 + l4^2 ...
       + 2*cos(phi4)*l4*l5 + l5^2)*(- l1^2 + 2*cos(phi1 - phi4)*l1*l4 + 2*cos(phi1)*l1*l5 + l2^2 + 2*l2*l3 ...
       + l3^2 - l4^2 - 2*cos(phi4)*l4*l5 - l5^2))^(1/2) - 2*l1*l2*sin(phi1) + 2*l2*l4*sin(phi4))/(l1^2 ...
       - 2*cos(phi1)*l1*l2 - 2*cos(phi1 - phi4)*l1*l4 - 2*cos(phi1)*l1*l5 + l2^2 + 2*cos(phi4)*l2*l4 + 2*l2*l5 ...
       - l3^2 + l4^2 + 2*cos(phi4)*l4*l5 + l5^2))) + l1*sin(phi1);
xd = l5 + l4*cos(phi4);
yd = l4*sin(phi4);
xe = l5;
ye = 0;


end